import os
import json
import requests
from mcp.server.fastmcp import FastMCP
from requests.auth import HTTPBasicAuth

# ==============================================================================
# Configuration & Credentials
# ==============================================================================
ATLASSIAN_DOMAIN = os.getenv("ATLASSIAN_DOMAIN", "your-domain.atlassian.net")
ATLASSIAN_EMAIL = os.getenv("ATLASSIAN_EMAIL", "agent@yourdomain.com")
ATLASSIAN_API_TOKEN = os.getenv("ATLASSIAN_API_TOKEN", "your_api_token")
JIRA_PROJECT_KEY = os.getenv("JIRA_PROJECT_KEY", "AGILE")

JIRA_BASE_URL = f"https://{ATLASSIAN_DOMAIN}/rest/api/3"
CONFLUENCE_BASE_URL = f"https://{ATLASSIAN_DOMAIN}/wiki/rest/api"

# Standard paths aligning with your workspace
BASE_DIR = "."
WIKI_DIR = os.path.join(BASE_DIR, "wiki")
STATE_FILE = os.path.join(WIKI_DIR, "project_state.json")
SYNTHESIS_FILE = os.path.join(WIKI_DIR, "DAILY_SYNTHESIS.md")

auth = HTTPBasicAuth(ATLASSIAN_EMAIL, ATLASSIAN_API_TOKEN)
headers = {
    "Accept": "application/json",
    "Content-Type": "application/json"
}

# Initialize the FastMCP Server for Atlassian
mcp = FastMCP("AtlassianIngress")

# ==============================================================================
# MCP RESOURCES (Read-Only Context)
# ==============================================================================

@mcp.resource("atlassian://jira/active-sprint")
def get_active_sprint_issues() -> str:
    """Fetches all incomplete issues from the current Jira project."""
    jql = f"project = {JIRA_PROJECT_KEY} AND statusCategory != Done"
    url = f"{JIRA_BASE_URL}/search"
    
    try:
        response = requests.get(url, headers=headers, auth=auth, params={"jql": jql, "maxResults": 50})
        response.raise_for_status()
        issues = response.json().get("issues", [])
        
        simplified_issues = [
            {
                "key": issue["key"],
                "summary": issue["fields"]["summary"],
                "status": issue["fields"]["status"]["name"]
            } for issue in issues
        ]
        return json.dumps(simplified_issues, indent=2)
    except Exception as e:
        return json.dumps({"error": f"Failed to fetch Jira sprint: {str(e)}"})

